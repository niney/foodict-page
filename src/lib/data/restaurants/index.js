// 전체 식당 목록
export const allRestaurants = [...whiteSpoonRestaurants, ...blackSpoonRestaurants]

// 백수저 식당만
export const getWhiteSpoonRestaurants = () => whiteSpoonRestaurants

// 흑수저 식당만
export const getBlackSpoonRestaurants = () => blackSpoonRestaurants

// 추천 식당 (미슐랭 + 인기 식당)
export const getFeaturedRestaurants = () => {
  const michelin = whiteSpoonRestaurants.filter(r => r.michelin > 0).slice(0, 4)
  const popular = [
    blackSpoonRestaurants.find(r => r.id === 117), // 솔밤
    blackSpoonRestaurants.find(r => r.id === 116), // 금룡
    whiteSpoonRestaurants.find(r => r.id === 10),  // 카덴
    blackSpoonRestaurants.find(r => r.id === 103), // 소바쥬
  ].filter(Boolean)
  return [...michelin, ...popular]
}

// ID로 식당 찾기
export const getRestaurantById = (id) => {
  return allRestaurants.find(r => r.id === id)
}

// 타입별 필터
export const getRestaurantsBySpoonType = (type) => {
  return allRestaurants.filter(r => r.spoonType === type)
}
